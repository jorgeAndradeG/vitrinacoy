    {
        $producto = Producto::findOrFail($id);
        $user = User::findOrFail($producto->id_mype);
        $precio_total = $producto->precio * $request->cantidad;
        return view('payment.payment')->with(["producto" => $producto, "user" => $user, "cantidad" => $request->cantidad, "precio_total" => $precio_total]);
   
    }

